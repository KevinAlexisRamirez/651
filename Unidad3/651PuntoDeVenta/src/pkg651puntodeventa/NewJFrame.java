    public void actualizar(){
        tblProductos.setModel(tableModel);
        while(tableModel.getRowCount()>0);{
        tableModel.removeRow(0);
    }
        for(Ventas v: ListaVentas){
            Object x[]=new Object[4];
            x[0] = v.getDescripcion(); 
            x[1] =v.getPrecio();
            x[2] =v.getCantidad(); 
            x[3] =v.getImporte(); 
            
            tableModel.addRow(x);
        }
    }
